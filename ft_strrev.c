char	*ft_strrev(char *str)
{
	int		i;
	int		k;
	char	temp;

	i = 0;
	while (str[i] != '\0')
		i++;
	i--;
	k = 0;
	while (i > k)
	{
		temp = str[i];
		str[i] = str[k];
		str[k] = temp;
		i--;
		k++;
	}
	return (str);
}
