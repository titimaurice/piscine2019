int		ft_iterative_factorial(int nb)
{
	int a;
	int i;

	a = 0;
	i = 1;
	if (nb >= 0 && nb < 13)
	{
		a = 1;
		while (i <= nb)
		{
			a *= i;
			i++;
		}
	}
	return (a);
}
